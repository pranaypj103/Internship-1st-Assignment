#Write a program to accept two numbers from the user, swap their values and display the result.

num1 = int(input('Enter the first number num1:\n'))
num2 = int(input('Enter the second number num2:\n'))

print('Before swap, the values of num1={0} and num2={1}'.format(num1,num2))
print('After swap, the values of num1={0} and num2={1}'.format(num2,num1))